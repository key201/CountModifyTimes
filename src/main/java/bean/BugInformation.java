public class BugInformation {
	
	String bugId;
	String bugProduct;
	String bugComp;
	String bugAssign;
	String bugStatus;
	String bugResolution;
	String bugSummary;
	String bugChangeDate;
	
	public String getBugId() {
		return bugId;
	}
	public void setBugId(String bugId) {
		this.bugId = bugId;
	}
	public String getBugProduct() {
		return bugProduct;
	}
	public void setBugProduct(String bugProduct) {
		this.bugProduct = bugProduct;
	}
	public String getBugComp() {
		return bugComp;
	}
	public void setBugComp(String bugComp) {
		this.bugComp = bugComp;
	}
	public String getBugAssign() {
		return bugAssign;
	}
	public void setBugAssign(String bugAssign) {
		this.bugAssign = bugAssign;
	}
	public String getBugStatus() {
		return bugStatus;
	}
	public void setBugStatus(String bugStatus) {
		this.bugStatus = bugStatus;
	}
	public String getBugResolution() {
		return bugResolution;
	}
	public void setBugResolution(String bugResolution) {
		this.bugResolution = bugResolution;
	}
	public String getBugSummary() {
		return bugSummary;
	}
	public void setBugSummary(String bugSummary) {
		this.bugSummary = bugSummary;
	}
	public String getBugChangeDate() {
		return bugChangeDate;
	}
	public void setBugChangeDate(String bugChangeDate) {
		this.bugChangeDate = bugChangeDate;
	}	
	
}
